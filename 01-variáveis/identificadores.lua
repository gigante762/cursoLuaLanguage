--[[ 

    Nomes de variáveis válidos ou indentificadores válidos

    Somente começam com letras ou _
    Não podem ter espaços nem caracteres especias

    -- Exemplos válidos

    minhaVariavel   MEUNOME     _OLA    _oi

    iajsoduwhasdlbnilUgdaiugdiug    a231s1212sd121s12ds12da

    i1  z76231 AND

    -- Exemplos inválidos

    co-body    2d   3d  alguma variavel
    
    árvore      e?sd    true

    ------------ Palavras reservadas --------------
    and       break     do        else      elseif    end
    false     for       function  goto      if        in
    local     nil       not       or        repeat    return
    then      true      until     while
    -----------------------------------------------
    
    "Lua é uma linguagem que diferencia minúsculas de maiúsculas: and é uma palavra reservada,
    mas And e AND são dois nomes válidos diferentes. 
    Como uma convenção, nomes começando com um sublinhado seguido por letras maiúsculas (tais como _VERSION)
    são reservados para variáveis usadas por Lua." 
        3.1 – Convenções Léxicas
    

    Assim como vimos uma conveção acima temou outas também
    quanto ao nome das variáveis.
    
    Variáveis que não vão ter o valor modificado, são conhecidas como constantes e são representadas
    com letras maiúsculas como CONSTANTE ou PI ou SEI_LA_O_QUE

    Podemos nomear variáveis seguindo alguns padrões
        camelCase: nomeCompostoQualquerCoisa
        snake_case: nome_composto
 ]]